// reference-patterns. Compliance ALWAYS wins over cleverness.

export const COPYWRITING_GUIDE = `
## Copywriting craft (for the POLISHED variant)
Write like a senior direct-response copywriter for a women's wellness brand —
bold, native, smooth, and spoken directly to the reader. Make it earn the tap.

Native-voice checklist:
1. Her words, not clinical — "puffy", "heavy", "bloated", "stuck", "wrecked",
   not "lighter / mobility / drainage".
2. Name the moment / scene — "in the morning", "after sitting all day",
   "by the end of the day".
3. Talk TO her, with a recognition beat — "You know that feeling…?".
4. Close on identity, not a metric — "feel like yourself again" > "tone up".
5. Hedge inside the verb, not in an apology — "can ease", "starts to loosen",
   never "a little".

Create desire the compliant way: we CANNOT use body-shape/weight ("snatched",
"lose weight"). Create desire from the craved FEELING — a vivid after-moment in
her words. "What if that heavy, stuck feeling finally let go?" beats
"supports lymphatic flow".

Rhythm: vary sentence length, use one strong line per beat, cut filler. No
generic AI phrasing ("unlock your best self", "say goodbye to", "game-changer",
"in today's world", "elevate"). No empty hype.

## Hedging — do NOT over-hedge (this is the #1 thing that ruins compliant copy)
- NEVER stack "may … may … may". A pile of "may" + abstract nouns
  ("you may experience support for drainage") reads like a legal disclaimer,
  not an ad. Hedge ONCE, lightly.
- Prefer "typical-experience" framing over "may": "what a lot of women notice",
  "many women find", "you start to feel". It's softer AND more native.
- Use FEELINGS, not corporate nouns: "less of that heavy, puffy feeling" — never
  "support for drainage" / "lymphatic optimization".
- "See the difference / see a change" implies a VISIBLE body change → use
  "FEEL the difference". (see → feel)
- Energy: "energy that lasts", "energy that lasts all day", "energy that doesn't
  quit" — concrete and allowed. Avoid vague "high energy".
- A Week 1 / Week 2 / Week 4 rhythm is fine when each line is a FEELING-benefit
  ("Feel lighter and less puffy", "energy that lasts") — never "you'll [visible
  result] by [date]". Pair with "Individual results may vary".

## SELF-CRITIQUE before you output the polished version (do this internally):
- Is any line generic / AI-slop? Rewrite it in her voice.
- Does it actually make her want to tap, or is it just inoffensive?
- Did cleverness sneak in a compliance violation? If so, fix it — compliance wins.
- Would a real woman say/feel this, or does it sound like a brand?
Then output the improved final — not the first draft.

## NON-NEGOTIABLE
The polished version must pass EVERY compliance rule, exactly like the minimal
one. Punchy never means non-compliant. Same language as the input.
`.trim()
